//-----------------------------------------------------------------------------
//! 読み込み
//!	@param	[in]	path ファイルのパス名
//-----------------------------------------------------------------------------
bool AssetTexture::load(const char* path)
{
	// 読み込みと転送
	if( _texture.loadAndTexImage2D(path) == false ) {
		return false;
	}
	return true;
}

