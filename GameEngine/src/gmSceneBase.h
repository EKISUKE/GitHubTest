//-----------------------------------------------------------------------------
//!	シーン基底
//-----------------------------------------------------------------------------
class SceneBase : public Base
{
public:
	GM_TYPE(SceneBase);

	//-------------------------------------------------------------
	//!	@name	初期化
	//-------------------------------------------------------------
	//@{

	//! コンストラクタ
	SceneBase();

	//! デストラクタ
	virtual ~SceneBase();

	//@}
	//-------------------------------------------------------------
	//!	@name	シーン関数
	//-------------------------------------------------------------
	//@{

	//! 開始
	virtual bool			init();

	//! 更新
	virtual void			update();

	//! フィルターの影響しない描画
	virtual void			drawNoFilter();

	//! 解放
	virtual void			cleanup();

	//@}
	//-------------------------------------------------------------
	//!	@name	取得・設定
	//-------------------------------------------------------------
	//@{

	//! 終了フラグ取得
	bool					isExit() const { return _isExit; }

	//@}
protected:
	bool					_isExit;	//!< 終了フラグ

};

