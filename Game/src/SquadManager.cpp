Squad* SquadManager::getSameSquad(Squad* search)
{
	for( list<Squad*>::iterator itr = _pSquadList.begin(); itr != _pSquadList.end(); itr++ )
	{
		Squad* tmpSqd = *itr;
		// Œ©‚Â‚©‚Á‚½‚ç
		if( tmpSqd == search )
		{
			return *itr;
		}
	}
	// Œ©‚Â‚©‚ç‚È‚©‚Á‚½
	return NULL;
}


