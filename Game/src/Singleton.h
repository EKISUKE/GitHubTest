//-----------------------------------------------------------------------------
//! シングルトン
//-----------------------------------------------------------------------------
template <typename T>
class Singleton
{

public:
	//-------------------------------------------------------------
	//! @name 初期化
	//-------------------------------------------------------------
	//@{

	//! コンストラクタ
	Singleton()
	{
	}

	//!	デストラクタ
	virtual ~Singleton()
	{
	}

public:
	//@}
	//-------------------------------------------------------------
	//! @name 取得・設定
	//-------------------------------------------------------------
	//@{

	//! 唯一のインスタンス獲得
	static T*	getInstance() { 
		static T _myIntance;
		return  &_myIntance; 
	}

	//@}
};


